uint8_t nyan_draw(uint8_t frame_num) {
    SSD1306_drawBitmap(0, 0, nyancat[frame_num], 128, 64);
    SSD1306_drawBufferDMA();
    delay_ms(70);
    return (frame_num + 1) % NYANCAT_NUM_FRAMES;
}


void app_nyan_func(void)
{
    uint8_t frame_num = 0;

    while(btn_state() != BUT_LEFT) {
        frame_num = nyan_draw(frame_num);
    }

    while(btn_state());    /* wait for the button release */
}

